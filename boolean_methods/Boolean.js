// The Boolean() function can be used to convert a value to a boolean value (true or false).

// Example usage of Boolean() function
let value1 = Boolean(0);    // false
let value2 = Boolean(1);    // true
let value3 = Boolean("");   // false
let value4 = Boolean("Hello"); // true

console.log(value1); // Output: false
console.log(value2); // Output: true
console.log(value3); // Output: false
console.log(value4); // Output: true