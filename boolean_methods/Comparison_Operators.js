// Comparison operators are used to compare two values and return a boolean result:

//  .Equal (==): Checks if values are equal.
//  .Strict Equal (===): Checks if values and types are equal.
//  .Not Equal (!=): Checks if values are not equal.
//  .Strict Not Equal (!==): Checks if values and types are not equal.
//  .Greater Than (>): Checks if the left value is greater than the right value.
//  .Less Than (<): Checks if the left value is less than the right value.
//  .Greater Than or Equal (>=): Checks if the left value is greater than or equal to the right value.
//  .Less Than or Equal (<=): Checks if the left value is less than or equal to the right value.

// Example usage of comparison operators
let x = 5;
let y = 10;

console.log(x == y);    // Equal operator - Output: false
console.log(x === y);   // Strict equal operator - Output: false
console.log(x != y);    // Not equal operator - Output: true
console.log(x !== y);   // Strict not equal operator - Output: true
console.log(x > y);     // Greater than operator - Output: false
console.log(x < y);     // Less than operator - Output: true
console.log(x >= y);    // Greater than or equal operator - Output: false
console.log(x <= y);    // Less than or equal operator - Output: true