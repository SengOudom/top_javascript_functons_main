// Logical operators are used to combine multiple boolean expressions or values:

//  .AND (&&): Returns true if both operands are true.
//  .OR (||): Returns true if at least one of the operands is true.
//  .NOT (!): Returns the inverse of the operand's boolean value.

// Example usage of logical operators
let a = true;
let b = false;

console.log(a && b); // AND operator - Output: false
console.log(a || b); // OR operator - Output: true
console.log(!a);     // NOT operator - Output: false