// The Number.parseInt() method parses a string argument and returns an integer of the specified radix (base).

console.log(Number.parseInt('10')); // 10
console.log(Number.parseInt('10.5')); // 10
console.log(Number.parseInt('10', 2)); // 2 (binary)
console.log(Number.parseInt('10', 8)); // 8 (octal)
console.log(Number.parseInt('10', 16)); // 16 (hexadecimal)